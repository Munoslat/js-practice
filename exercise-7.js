let colors = [ "orange", "pink", "blue" ];

console.log(colors);

console.log("after push"),
colors.push("black");
console.log(colors);

console.log("after pop"),
colors.pop("");
console.log(colors);

console.log("after shift"),
colors.shift("");
console.log(colors);

console.log("after unshift"),
colors.unshift("orange");
console.log(colors);


